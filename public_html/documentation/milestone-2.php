<h1> Content Strategy </h1>
<p>My site will be one page, and will consist of the following sections:</p>

<h2> Welcome: </h2>
<p> The welcome section will have a brief introduction of myself that specifies that I am a full-stack developer as well
	as a head shot of myself (jpeg).</p>

<h2> About Me: </h2>
<p>This section will detail my educational & professional history as well as my future education & aspirations of
	being involved in the tech community while helping others
	achieve their aspirations and goals.</p>
<p>I will also feature a simple bullet point or like list that quickly displays core skills, programming languages
	and goals at a glance. </p>
<p> The section will express that I am interested in helping others achieve their goals, achieve a professional image,
	and display their work/portfolio as they would like their clientele to view them. </p>

<h2> Portfolio: </h2>
<p> I want this to be a simple, clean image gallery containing 2-3 examples of my projects with a quick summary of my
	role of each and technology/programs used in each</p>


<h2> Contact: </h2>
<p> This section will contain a simple contact form, my professional email address, and links to my professional
	profiles: </p>
<p> LinkedIn & GitHub which will be linked to the footer on the mobile view while formatted to the left as a column on
	the desktop site. </p>


